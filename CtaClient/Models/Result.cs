namespace CtaClient.Models;

/// <summary>
///   Provides information about the result of a CTA API call.
/// </summary>
public class Result<T>
{
    private Result(Error error)
    {
        IsSuccess = false;
        Error = error;
    }

    private Result(T value)
    {
        IsSuccess = true;
        Error = Error.None;
        Value = value;
    }

    /// <summary>
    ///   Gets a value that indicates whether the call executed successfully or not.
    /// </summary>
    /// 
    public bool IsSuccess { get; }

    /// <summary>
    ///   Gets a value that indicates whether the call failed or not.
    /// </summary>
    public bool IsFailure => !IsSuccess;

    /// <summary>
    ///   Gets the Error that was generated by a failed call.
    /// </summary>
    public Error Error { get; }

    /// <summary>
    ///   Gets the result value of a successful call.
    /// </summary>
    public T? Value { get; }

    /// <summary>
    ///   Generates a successful result.
    /// </summary>
    public static Result<T> Success(T value) => new(value);

    /// <summary>
    ///   Generates an error result.
    /// </summary>
    public static Result<T> Failure(Error error) => new(error);
}
